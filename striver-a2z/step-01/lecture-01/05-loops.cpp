#include<bits/stdc++.h>
using namespace std;

int main (){

    // for loop
    for(int i = 1; i<10; i++){
        cout << "google" << endl;
    }

    // while loop
    int j;//initiation
    while (j<=5) {//condition
        cout << "wow" << endl;//print
        j = j+1;//incrementation
    }
    return 0;

    // do while
    int k = 2;
    do{
        cout << "yahoo" << endl;
    }while(k<=1);
    cout << k << endl;
}